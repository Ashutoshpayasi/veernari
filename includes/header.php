<script>
// Show feedback button on scroll
// Static feedback comments related to NGO "Veer Nari Shakti"
let feedbackArray = [
    "Great initiative for empowering women!",
    "I love the work you're doing for the community.",
    "More support should be given to such NGOs!",
    "This platform is truly inspiring!",
    "The change you bring is incredible!"
];

// Show feedback button on scroll
window.onscroll = function () {
    let feedbackBtn = document.getElementById("feedbackButton");
    if (document.documentElement.scrollTop > 100) {
        feedbackBtn.style.display = "inline-block";
    } else {
        feedbackBtn.style.display = "none";
    }
};

// Open modal and load comments
document.getElementById("feedbackButton").addEventListener("click", function () {
    document.getElementById("feedbackModal").style.display = "block";
    loadFeedback();
});

// Close modal
function closeFeedbackModal() {
    document.getElementById("feedbackModal").style.display = "none";
}

// Function to load feedback
function loadFeedback() {
    let feedbackList = document.getElementById("feedbackList");
    feedbackList.innerHTML = ""; // Clear previous content

    feedbackArray.forEach(comment => {
        let feedbackItem = document.createElement("div");
        feedbackItem.className = "feedback-item";
        feedbackItem.innerHTML = `<p>${comment}</p>`;
        feedbackList.appendChild(feedbackItem);
    });
}

// Add feedback dynamically
function addFeedback() {
    let feedbackInput = document.getElementById("feedbackInput");

    if (feedbackInput.value.trim() !== "") {
        feedbackArray.unshift(feedbackInput.value); // Add new feedback to the beginning
        feedbackInput.value = ""; // Clear input
        loadFeedback(); // Reload feedback list
    }
}
</script>